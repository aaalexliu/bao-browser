// Bao M0 spike — capture + deterministic replay on a single page.
// No LLM, no backend, no cross-navigation. Proves the risky core:
// selector generation, multi-selector fallback resolution, and the input actuator.

(() => {
  "use strict";

  // Idempotent: the manifest auto-injects this, but the harness may also inject
  // it programmatically (chrome.scripting) as a readiness fallback. Bail on a
  // second run so we don't register a duplicate onMessage listener.
  if (window.__baoM0Loaded) return;
  window.__baoM0Loaded = true;

  // ---------- shared helpers ----------
  const sleep = (ms) => new Promise((r) => setTimeout(r, ms));
  const esc = (s) => (window.CSS && CSS.escape ? CSS.escape(s) : s.replace(/[^a-zA-Z0-9_-]/g, "\\$&"));

  function isTextField(el) {
    return el instanceof HTMLTextAreaElement ||
      (el instanceof HTMLInputElement &&
        !["checkbox", "radio", "button", "submit", "file", "range"].includes(el.type));
  }

  function isVisible(el) {
    const r = el.getBoundingClientRect();
    if (r.width === 0 && r.height === 0) return false;
    const s = getComputedStyle(el);
    return s.visibility !== "hidden" && s.display !== "none";
  }

  // IDs like "ember123", ":r1:", "mui-3481", "radix-:r0:", uuids → unstable, skip.
  function looksAutoGenerated(id) {
    return /\d{3,}/.test(id) || /[:]/.test(id) || /^[a-f0-9]{8}-/.test(id) || /^[a-z]+-?\d+$/i.test(id);
  }

  function implicitRole(el) {
    const tag = el.nodeName.toLowerCase();
    if (tag === "button") return "button";
    if (tag === "a" && el.hasAttribute("href")) return "link";
    if (tag === "select") return "combobox";
    if (tag === "textarea") return "textbox";
    if (tag === "input") {
      const t = (el.getAttribute("type") || "text").toLowerCase();
      if (["checkbox"].includes(t)) return "checkbox";
      if (["radio"].includes(t)) return "radio";
      if (["submit", "button"].includes(t)) return "button";
      return "textbox";
    }
    return el.getAttribute("role") || null;
  }

  // Approximate accessible name (good enough for M0).
  function accName(el) {
    const aria = el.getAttribute("aria-label");
    if (aria) return aria.trim();
    if (el.getAttribute("placeholder")) return el.getAttribute("placeholder").trim();
    if (el.labels && el.labels[0]) return el.labels[0].textContent.trim();
    if (el.value && (el.type === "submit" || el.type === "button")) return el.value.trim();
    const txt = (el.textContent || "").replace(/\s+/g, " ").trim();
    return txt.slice(0, 80);
  }

  // ---------- selector generation (ranked, multi-candidate) ----------
  function getSelectors(el) {
    const sels = [];
    for (const attr of ["data-testid", "data-test", "data-cy", "data-qa"]) {
      const v = el.getAttribute(attr);
      if (v) sels.push({ type: "testid", value: `[${attr}="${esc(v)}"]`, score: 0.95 });
    }
    const role = implicitRole(el);
    const name = accName(el);
    if (role && name && name.length <= 60) {
      sels.push({ type: "aria", value: JSON.stringify({ role, name }), score: 0.9 });
    }
    if (name && ["button", "link"].includes(role)) {
      sels.push({ type: "text", value: name, score: 0.7 });
    }
    if (el.id && !looksAutoGenerated(el.id)) {
      sels.push({ type: "id", value: `#${esc(el.id)}`, score: 0.8 });
    }
    sels.push({ type: "css", value: cssPath(el), score: 0.3 });
    return sels.sort((a, b) => b.score - a.score);
  }

  function cssPath(el) {
    const parts = [];
    while (el && el.nodeType === 1 && el !== document.body) {
      if (el.id && !looksAutoGenerated(el.id)) { parts.unshift(`#${esc(el.id)}`); break; }
      let nth = 1, sib = el;
      while ((sib = sib.previousElementSibling)) if (sib.nodeName === el.nodeName) nth++;
      parts.unshift(`${el.nodeName.toLowerCase()}:nth-of-type(${nth})`);
      el = el.parentElement;
    }
    return parts.join(" > ");
  }

  // ---------- shadow-DOM piercing (capture across OPEN shadow boundaries) ----------
  // A css path for `node` scoped to `root` (a Document or ShadowRoot). Walking
  // parentElement naturally stops at a shadow root's top child (whose parentElement is
  // null), so the path stays relative to the root the node actually lives in.
  function scopedCssPath(node, root) {
    const parts = [];
    let el = node;
    while (el && el.nodeType === 1) {
      if (el.id && !looksAutoGenerated(el.id)) { parts.unshift(`#${esc(el.id)}`); break; }
      let nth = 1, sib = el;
      while ((sib = sib.previousElementSibling)) if (sib.nodeName === el.nodeName) nth++;
      parts.unshift(`${el.nodeName.toLowerCase()}:nth-of-type(${nth})`);
      const parent = el.parentElement;
      if (!parent || parent === document.body) break;
      el = parent;
    }
    return parts.join(" > ");
  }
  // The most stable selector for `node` that's unique *within its own root* — the
  // segment we use to hop one shadow boundary.
  function segmentSelector(node, root) {
    for (const attr of ["data-testid", "data-test", "data-cy", "data-qa"]) {
      const v = node.getAttribute(attr);
      if (v) { const sel = `[${attr}="${esc(v)}"]`; if (root.querySelectorAll(sel).length === 1) return sel; }
    }
    if (node.id && !looksAutoGenerated(node.id)) {
      const sel = `#${esc(node.id)}`; if (root.querySelectorAll(sel).length === 1) return sel;
    }
    return scopedCssPath(node, root);
  }
  // If `el` lives inside one or more OPEN shadow roots, return the ordered list of
  // per-boundary segment selectors (outermost host → … → el); null for light DOM.
  // Closed roots never reach here: composedPath stops at their host, so at capture
  // time we only ever hold light-DOM or open-shadow elements.
  function shadowSegments(el) {
    let root = el.getRootNode();
    if (!(root instanceof ShadowRoot)) return null;
    const segs = [];
    let node = el;
    while (root instanceof ShadowRoot) {
      segs.unshift(segmentSelector(node, root));
      node = root.host;
      root = node.getRootNode();
    }
    segs.unshift(segmentSelector(node, document)); // outermost host, in light DOM
    return segs;
  }
  // Resolve a shadow-piercing path by descending through each host's open shadow root.
  // Returns [el] or [] — the count-aware shape matchesOf expects.
  function resolveShadowPath(segs) {
    let ctx = document, el = null;
    for (const seg of segs) {
      el = ctx.querySelector(seg);
      if (!el) return [];
      ctx = el.shadowRoot || el;
    }
    return el ? [el] : [];
  }
  // Classify how reachable a captured element is — drives graceful degrade. We only
  // ever see light or open-shadow leaves; an opaque custom element (hyphenated tag,
  // no open shadowRoot, no light children) is the closed-shadow tell, and <canvas> is
  // pixels. Both degrade (no clean selector) instead of recording a confident lie.
  function reachOf(el) {
    if (el.getRootNode() instanceof ShadowRoot) return "open-shadow";
    if (el.nodeName === "CANVAS") return "canvas";
    if (el.localName && el.localName.includes("-") && !el.shadowRoot && el.childElementCount === 0)
      return "opaque-custom";
    return "light";
  }

  // ---------- capture-time anchoring (escalate context until unique) ----------
  // Extract a *stable* token from an href: a note/comment id (`c-12345`), a post
  // slug (`/p/foo`), or any path segment carrying digits. Query-string junk and
  // session ids are skipped by preferring these shapes.
  function stableHrefId(href) {
    if (!href) return null;
    const m = href.match(/c-\d+/) || href.match(/\/p\/[\w-]+/) || href.match(/\/[\w-]*\d[\w-]*/);
    return m ? m[0] : null;
  }
  // A selector that identifies a NODE by its own attributes, only if unique on page.
  function nodeUniqueSelector(node) {
    if (node.id && !looksAutoGenerated(node.id)) {
      const sel = `#${esc(node.id)}`;
      if (document.querySelectorAll(sel).length === 1) return sel;
    }
    for (const attr of ["data-testid", "data-test", "data-cy", "data-qa"]) {
      const v = node.getAttribute(attr);
      if (v) {
        const sel = `[${attr}="${esc(v)}"]`;
        if (document.querySelectorAll(sel).length === 1) return sel;
      }
    }
    return null;
  }
  // The smallest distinguishing signature inside `node`'s subtree: a unique link id.
  function uniqueHrefIn(node) {
    for (const a of node.querySelectorAll("a[href]")) {
      const id = stableHrefId(a.getAttribute("href"));
      if (id && document.querySelectorAll(`a[href*="${id.replace(/"/g, '\\"')}"]`).length === 1) return id;
    }
    return null;
  }
  // Last-resort signature for cards that carry no stable id/href (image-only
  // restacks, etc.): a short text fingerprint whose *exact* occurrences all live
  // inside `node` — i.e. unique to this card. Skips the target's own text and any
  // text it shares with sibling cards ("Like", "Reply", …).
  const normText = (el) => (el.textContent || "").replace(/\s+/g, " ").trim();
  function uniqueTextIn(node, leaf) {
    const seen = new Set();
    for (const el of node.querySelectorAll("*")) {
      if (el === leaf || el.contains(leaf) || leaf.contains(el)) continue;
      const t = normText(el);
      if (t.length < 3 || t.length > 80 || seen.has(t)) continue;
      seen.add(t);
      const exact = Array.from(document.querySelectorAll("*")).filter((e) => normText(e) === t);
      if (exact.length && exact.every((e) => node.contains(e))) return t;
    }
    return null;
  }

  // Walk up from the target to the nearest ancestor we can pin down uniquely, and
  // describe the target *within* it. This is what turns an ambiguous "Share" into
  // "the Share button inside the card linking to c-276371955".
  function buildAnchor(leaf) {
    let node = leaf.parentElement;
    for (let hops = 0; node && node !== document.body && hops < 15; hops++, node = node.parentElement) {
      const sel = nodeUniqueSelector(node);
      if (sel) return { node, descriptor: { kind: "selector", value: sel } };
      const href = uniqueHrefIn(node);
      if (href) return { node, descriptor: { kind: "href", id: href } };
      const txt = uniqueTextIn(node, leaf);
      if (txt) return { node, descriptor: { kind: "text", id: txt } };
    }
    return null;
  }
  // The nearest scrollable ancestor — the viewport a virtualized list windows over.
  // Captured so replay can scroll-find a target that isn't yet in the DOM.
  function scrollableAncestor(el) {
    for (let n = el.parentElement; n && n !== document.body; n = n.parentElement) {
      const s = getComputedStyle(n);
      if (/(auto|scroll)/.test(s.overflowY + s.overflow) && n.scrollHeight > n.clientHeight + 4) return n;
    }
    return null;
  }
  // A stable selector for a single node (id if usable, else a scoped css path).
  function nodeSelector(node) {
    if (node.id && !looksAutoGenerated(node.id) && document.querySelectorAll(`#${esc(node.id)}`).length === 1)
      return `#${esc(node.id)}`;
    return cssPath(node);
  }

  // A css path from `root` down to `leaf` (root exclusive). Stable while the card's
  // *internal* structure holds, even as the feed around it reorders.
  function relPath(root, leaf) {
    const parts = [];
    let el = leaf;
    while (el && el !== root && el.nodeType === 1) {
      let nth = 1, sib = el;
      while ((sib = sib.previousElementSibling)) if (sib.nodeName === el.nodeName) nth++;
      parts.unshift(`${el.nodeName.toLowerCase()}:nth-of-type(${nth})`);
      el = el.parentElement;
    }
    return el === root ? parts.join(" > ") : null;
  }
  // A content-stable discriminator that singles `leaf` out from its same-named
  // siblings — its own attribute, independent of position.
  function refineWithin(matches, leaf) {
    for (const attr of ["data-testid", "data-test", "data-cy", "data-qa", "title", "name", "href"]) {
      const v = leaf.getAttribute(attr);
      if (v && matches.filter((m) => m.getAttribute(attr) === v).length === 1) return { attr, value: v };
    }
    return null;
  }
  function describeWithin(anchorNode, leaf) {
    const role = implicitRole(leaf);
    const name = accName(leaf);
    const matches = allByWithin({ role, name }, anchorNode);
    const within = { role, name, index: Math.max(0, matches.indexOf(leaf)) };
    // Only when the role+name is itself ambiguous inside the card do we need a
    // tiebreak: a distinguishing attribute first, a structural path second.
    if (matches.length > 1) {
      const attr = refineWithin(matches, leaf);
      if (attr) within.attr = attr;
      const rel = relPath(anchorNode, leaf);
      if (rel) within.rel = rel;
    }
    return within;
  }

  // True iff selector `s` resolves to exactly the captured element (same path replay
  // will take). If no robust selector is unique, we must anchor.
  function resolvesUniquelyTo(s, el) {
    const m = matchesOf(s);
    return m.length === 1 && m[0] === el;
  }

  // The recorded target: leaf selectors, plus an anchor + within-descriptor whenever
  // the leaf alone can't be resolved back to the exact element.
  function getTarget(el) {
    const reach = reachOf(el);
    // Pixels (canvas) or an opaque custom element (likely closed shadow): we can't
    // build a clean selector. Capture bbox as Tier-2/3 fuel and degrade honestly.
    if (reach === "canvas" || reach === "opaque-custom") {
      const r = el.getBoundingClientRect();
      const bbox = { x: r.left, y: r.top, w: r.width, h: r.height };
      return { selectors: getSelectors(el), reach, bbox, unique: false, degraded: true };
    }
    const selectors = getSelectors(el);
    // Open-shadow leaf: prepend a shadow-piercing path so resolution can re-find it
    // (plain document.querySelector can't cross the boundary).
    const segs = shadowSegments(el);
    if (segs) selectors.unshift({ type: "shadowpath", value: JSON.stringify(segs), score: 0.92 });

    // A scrollable ancestor means the target may be virtualized off-screen at replay;
    // record the viewport so resolution can scroll-find it. (Additive — absent on
    // ordinary pages.)
    const sc = scrollableAncestor(el);
    const base = { selectors, reach };
    if (sc) base.scroll = { container: nodeSelector(sc) };

    const robustUnique = selectors.some((s) => s.type !== "css" && resolvesUniquelyTo(s, el));
    if (robustUnique) return { ...base, unique: true };

    const anchor = buildAnchor(el);
    if (anchor) {
      const within = describeWithin(anchor.node, el);
      return { ...base, anchor: anchor.descriptor, within, unique: true };
    }
    return { ...base, unique: false }; // best-effort: brittle css path only
  }

  // ---------- count-aware resolution ----------
  // Every resolver returns ALL matches (optionally scoped to a root subtree) so the
  // caller can reason about ambiguity instead of blindly taking the first hit — the
  // root cause of the "wrong item in a repeated list" bug.
  function allByAria({ role, name }, root = document) {
    return Array.from(root.querySelectorAll("*"))
      .filter((el) => implicitRole(el) === role && accName(el) === name);
  }
  function allByText(value, root = document) {
    return Array.from(root.querySelectorAll('button, a, [role="button"], input[type="submit"], summary'))
      .filter((el) => accName(el) === value);
  }
  function matchesOf(s, root = document) {
    try {
      if (s.type === "shadowpath") return resolveShadowPath(JSON.parse(s.value));
      if (["testid", "id", "css"].includes(s.type)) return Array.from(root.querySelectorAll(s.value));
      if (s.type === "aria") return allByAria(JSON.parse(s.value), root);
      if (s.type === "text") return allByText(s.value, root);
    } catch (_) {}
    return [];
  }
  // A selector "within" the anchor subtree: role+name, picked by recorded index.
  function allByWithin({ role, name }, root) {
    return Array.from(root.querySelectorAll("*"))
      .filter((el) => implicitRole(el) === role && (!name || accName(el) === name));
  }

  // ---------- anchor resolution (the robust path for repeated lists) ----------
  // Re-find the anchor's stable "seed" (a unique id'd container, or a unique link/
  // text deeper in the card), then climb to the smallest ancestor that actually
  // contains the target — content-anchored, so it survives feed reorders.
  function anchorSeed(a) {
    if (a.kind === "selector") return document.querySelector(a.value);
    if (a.kind === "href") return document.querySelector(`a[href*="${a.id.replace(/"/g, '\\"')}"]`);
    if (a.kind === "text") {
      const all = Array.from(document.querySelectorAll("*"));
      // Prefer an element whose exact text IS the fingerprint; fall back to any
      // element containing it. Smallest wins so the seed lands deep in one card.
      const exact = all.filter((el) => normText(el) === a.id);
      const pool = exact.length ? exact : all.filter((el) => (el.textContent || "").includes(a.id));
      return pool.sort((x, y) => x.textContent.length - y.textContent.length)[0] || null;
    }
    return null;
  }
  function resolveViaAnchor(target) {
    const seed = anchorSeed(target.anchor);
    if (!seed) return null;
    const within = target.within;
    let node = seed;
    for (let i = 0; i < 15 && node; i++) {
      const m = allByWithin(within, node);
      if (m.length) {
        if (m.length === 1) return m[0];
        // Collision inside the anchor: content-stable attribute beats structural
        // path beats raw positional index.
        if (within.attr) {
          const hit = m.find((el) => el.getAttribute(within.attr.attr) === within.attr.value);
          if (hit) return hit;
        }
        if (within.rel) { try { const r = node.querySelector(within.rel); if (r) return r; } catch (_) {} }
        return m[within.index] || m[0];
      }
      node = node.parentElement;
    }
    return null;
  }

  function resolveStep(target) {
    // 1) Anchor-scoped resolution wins — it's the only path that disambiguates one
    //    "Copy link" from its 20 identical siblings.
    if (target.anchor && target.within) {
      const el = resolveViaAnchor(target);
      if (el) return { el, via: "anchor" };
    }
    // 2) Plain selectors, but PREFER a uniquely-resolving one over first-of-many.
    //    A selector that matches the whole list must not outrank a precise one just
    //    because its `type` scores high.
    let ambiguous = null;
    for (const s of target.selectors) {
      const m = matchesOf(s);
      if (m.length === 1) return { el: m[0], via: s.type };
      if (m.length > 1 && !ambiguous) ambiguous = { el: m.find(isVisible) || m[0], via: `${s.type}~ambiguous` };
    }
    return ambiguous || { el: null };
  }
  // "Good enough to commit to." A first-of-many ambiguous match never is. And when
  // the target carried an anchor (it was ambiguous at capture), only an anchor hit is
  // trustworthy — a unique css/positional match is exactly the recycled-row lie that
  // virtualization creates, so it must not preempt the anchor.
  function isGood(r, target) {
    if (!r.el || !isVisible(r.el)) return false;
    if (String(r.via || "").includes("ambiguous")) return false;
    if (target.anchor && r.via !== "anchor") return false;
    return true;
  }

  // Scroll the captured viewport top-to-bottom, re-resolving after each step, until
  // the anchor pins the target. Only an anchor hit stops the scan (off-screen rows
  // resolve to whatever's on screen). Linear scan is the honest fallback; a
  // uniform-row binary search is a later optimization.
  async function scrollFind(target) {
    const c = document.querySelector(target.scroll.container);
    if (!c) return { el: null };
    const step = Math.max(64, c.clientHeight * 0.7);
    const restore = c.scrollTop;
    for (let y = 0, i = 0; y <= c.scrollHeight && i < 3000; y += step, i++) {
      c.scrollTop = y;
      await sleep(30); // let the windowed list render the new slice
      const r = resolveStep(target);
      if (isGood(r, target)) return r;
    }
    c.scrollTop = restore;
    return { el: null };
  }
  async function waitForStep(target, timeout = 5000) {
    const start = Date.now();
    while (Date.now() - start < timeout) {
      const r = resolveStep(target);
      if (isGood(r, target)) return r;
      // Off-screen in a virtualized list: scroll-find before settling for anything.
      if (target.scroll) {
        const sr = await scrollFind(target);
        if (sr.el) return sr;
      }
      if (r.el && isVisible(r.el)) return r; // last resort: a unique-but-imperfect hit
      await sleep(100);
    }
    return { el: null };
  }

  // ---------- the input actuator (the #1 silent killer) ----------
  function setNativeValue(el, value) {
    const proto = el instanceof HTMLTextAreaElement
      ? HTMLTextAreaElement.prototype : HTMLInputElement.prototype;
    const setter = Object.getOwnPropertyDescriptor(proto, "value").set;
    setter.call(el, value);
    el.dispatchEvent(new InputEvent("input", { bubbles: true }));
    el.dispatchEvent(new Event("change", { bubbles: true }));
  }
  // Native <select>: option list is OS-drawn, so set value + fire the events the page
  // listens for (frameworks key off `change`).
  function setSelectValue(el, value) {
    el.value = value;
    el.dispatchEvent(new Event("input", { bubbles: true }));
    el.dispatchEvent(new Event("change", { bubbles: true }));
  }
  // contenteditable actuator. setNativeValue can't drive these — there is no value
  // setter. Attempt order: (1) execCommand insertText (deprecated, but still the only
  // synthetic path most editors accept — it routes through the same cancelable
  // beforeinput a real keystroke produces, so model-driven editors like ProseMirror/
  // Lexical apply it to their model); (2) synthetic beforeinput+input dispatch;
  // (3) verify the text landed, else report failure. Heavily custom editors (Google
  // Docs is canvas anyway) may reject all synthetic paths — the honest failure is
  // correct behavior, not a bug.
  const squish = (s) => (s || "").replace(/\s+/g, " ").trim();
  async function setEditableValue(el, value) {
    const landed = () => squish(el.innerText).includes(squish(value));
    el.focus();
    try {
      document.execCommand("selectAll", false, null);
      document.execCommand("insertText", false, value);
    } catch (_) {}
    await sleep(50);
    if (landed()) return true;
    try {
      el.dispatchEvent(new InputEvent("beforeinput", {
        bubbles: true, cancelable: true, inputType: "insertText", data: value,
      }));
      el.dispatchEvent(new InputEvent("input", {
        bubbles: true, inputType: "insertText", data: value,
      }));
    } catch (_) {}
    await sleep(50);
    return landed();
  }

  // ---------- highlight overlay (the "watch it" feel) ----------
  function highlight(el) {
    const r = el.getBoundingClientRect();
    const box = document.createElement("div");
    Object.assign(box.style, {
      position: "fixed", left: r.left + "px", top: r.top + "px",
      width: r.width + "px", height: r.height + "px",
      border: "2px solid #e11", borderRadius: "3px", zIndex: 2147483647,
      pointerEvents: "none", boxShadow: "0 0 0 3px rgba(238,17,17,.25)",
      transition: "opacity .4s", opacity: "1",
    });
    document.documentElement.appendChild(box);
    setTimeout(() => { box.style.opacity = "0"; setTimeout(() => box.remove(), 400); }, 500);
  }

  // ---------- recording ----------
  let recording = false, steps = [], lastInputStep = null, lastInputEl = null;
  let lastStepUrl = null;

  // SPA soft-navigation marker (T7). Rich SaaS apps route via history.pushState —
  // the content script survives (no teardown), so we detect the route change by
  // comparing location.href step-over-step and insert a synthetic marker the replay
  // can wait on. urlPattern wildcards digit runs: the id minted at record time is
  // not the id at replay time ("create then open" flows).
  function urlPatternOf(u) {
    return u.replace(/[.*+?^${}()|[\]\\]/g, "\\$&").replace(/\d+/g, "\\d+");
  }
  function markSoftNav() {
    if (location.href === lastStepUrl) return;
    steps.push({
      action: "softNav", label: `Route changed to ${location.href}`,
      urlAfter: location.href, urlPattern: urlPatternOf(location.href),
      frame: frameInfo(), ts: Date.now(),
    });
    lastStepUrl = location.href;
  }
  function pushStep(step) { markSoftNav(); steps.push(step); }

  function humanLabel(action, el) {
    const name = accName(el) || el.nodeName.toLowerCase();
    if (action === "input") return `Type into "${name}"`;
    if (action === "select") return `Choose an option in "${name}"`;
    return `Click "${name}"`;
  }
  // Which frame this step was captured in. The content script runs per-frame
  // (all_frames), so a cross-origin child records its own steps relative to its own
  // document; the SW stitches frames together by frameId at stop time.
  function frameInfo() {
    return { url: location.href, origin: location.origin, top: window === window.top };
  }
  function makeStep(action, el) {
    return { action, label: humanLabel(action, el), target: getTarget(el), frame: frameInfo(), ts: Date.now() };
  }

  // The real interactive element, piercing OPEN shadow boundaries. `event.target` is
  // retargeted to the shadow host; `composedPath()` exposes the true inner node and
  // its ancestors across roots, so we pick the nearest interactive one.
  const INTERACTIVE = 'button,a,[role="button"],input,select,textarea,summary,[onclick]';
  function composedLeaf(e) {
    const path = e.composedPath ? e.composedPath() : [e.target];
    for (const n of path) {
      if (n instanceof Element && n.matches && n.matches(INTERACTIVE)) return n;
    }
    return path[0] instanceof Element ? path[0] : e.target;
  }

  function onClick(e) {
    const el = composedLeaf(e);
    if (el.nodeName === "SELECT") return; // captured via change, not click
    lastInputEl = lastInputStep = null;
    pushStep(makeStep("click", el));
  }
  // The editable ROOT the event belongs to. Editors nest contenteditable nodes, so
  // walk to the highest contentEditable ancestor — that's the stable, addressable
  // surface (the leaf under the caret churns per keystroke).
  function editableRoot(node) {
    let el = node instanceof Element ? node : node && node.parentElement;
    if (!el || !el.isContentEditable) return null;
    while (el.parentElement && el.parentElement.isContentEditable) el = el.parentElement;
    return el;
  }
  // Coalesced contenteditable capture: one step per editable root per typing burst,
  // value = innerText (we replay semantics, not markup).
  function recordEditable(root) {
    if (!recording) return;
    const value = root.innerText;
    if (lastInputEl === root && lastInputStep) { lastInputStep.value = value; return; }
    const step = makeStep("input", root);
    step.mode = "contenteditable"; step.value = value;
    pushStep(step); lastInputStep = step; lastInputEl = root;
  }
  function onInput(e) {
    const el = e.composedPath ? e.composedPath()[0] : e.target;
    const root = editableRoot(el);
    if (root) { recordEditable(root); return; }
    if (!(el instanceof Element) || !isTextField(el)) return;
    if (lastInputEl === el && lastInputStep) { lastInputStep.value = el.value; return; }
    const step = makeStep("input", el); step.value = el.value;
    pushStep(step); lastInputStep = step; lastInputEl = el;
  }
  // Strict model-driven editors (Lexical-style) preventDefault their beforeinput and
  // mutate the DOM themselves — so no native `input` event ever fires. Capture via
  // beforeinput too, reading the root's text on the next tick, after the editor (or
  // the browser default) has applied the change. recordEditable coalesces, so the
  // overlap with onInput on ordinary editables is harmless.
  function onBeforeInput(e) {
    const root = editableRoot(e.composedPath ? e.composedPath()[0] : e.target);
    if (!root) return;
    setTimeout(() => recordEditable(root), 0);
  }
  function onChange(e) {
    const el = e.composedPath ? e.composedPath()[0] : e.target;
    if (!(el instanceof Element) || el.nodeName !== "SELECT") return;
    const step = makeStep("select", el); step.value = el.value;
    pushStep(step); lastInputEl = lastInputStep = null;
  }

  function startRecording() {
    steps = []; lastInputEl = lastInputStep = null; lastStepUrl = location.href; recording = true;
    document.addEventListener("click", onClick, true);
    document.addEventListener("input", onInput, true);
    document.addEventListener("beforeinput", onBeforeInput, true);
    document.addEventListener("change", onChange, true);
  }
  function stopRecording() {
    markSoftNav(); // a route change after the last step still gets its marker
    recording = false;
    document.removeEventListener("click", onClick, true);
    document.removeEventListener("input", onInput, true);
    document.removeEventListener("beforeinput", onBeforeInput, true);
    document.removeEventListener("change", onChange, true);
    // Report this frame's steps to the SW so it can merge them with sibling/child
    // frames (which the top frame can't see across an origin boundary). Best-effort.
    if (steps.length) {
      try { chrome.runtime.sendMessage({ cmd: "bao-frame-steps", steps }).catch(() => {}); } catch (_) {}
    }
    return steps;
  }

  // ---------- replay ----------
  // Wait for a recorded soft navigation to settle: the URL must match the recorded
  // pattern (digit runs wildcarded — replay-time ids differ) before the next step's
  // element resolution starts, instead of leaning on the generic 5s retry.
  async function waitForUrl(step, timeout = 5000) {
    let re = null;
    try { re = new RegExp(`^${step.urlPattern}$`); } catch (_) {}
    const matches = () => (re ? re.test(location.href) : location.href === step.urlAfter);
    const start = Date.now();
    while (Date.now() - start < timeout) {
      if (matches()) return true;
      await sleep(100);
    }
    return matches();
  }
  async function replay(stepList) {
    const results = [];
    for (let i = 0; i < stepList.length; i++) {
      const step = stepList[i];
      if (step.action === "softNav") {
        const ok = await waitForUrl(step);
        if (!ok) {
          results.push({ i, ok: false, reason: `Route never matched ${step.urlAfter} (now: ${location.href})` });
          return { ok: false, failedAt: i, results };
        }
        results.push({ i, ok: true, via: "softNav" });
        continue;
      }
      // Degraded capture (canvas / closed shadow): no clean selector exists. Fail
      // cleanly rather than guess — this is where a Tier-3/4 executor takes over.
      if (step.target && step.target.degraded) {
        results.push({ i, ok: false, reason: `Needs escalation (reach=${step.target.reach}): ${step.label}` });
        return { ok: false, failedAt: i, results };
      }
      const r = await waitForStep(step.target);
      if (!r.el) {
        results.push({ i, ok: false, reason: `Could not find: ${step.label}` });
        return { ok: false, failedAt: i, results }; // fail with clear report
      }
      let el = r.el;
      el.scrollIntoView({ block: "center", behavior: "instant" });
      await sleep(150);
      // Virtualized lists recycle nodes on scroll, so the node we resolved may now
      // render *different* data after scrollIntoView settles. Re-resolve via the
      // content anchor so we act on the node that currently shows the target.
      if (step.target.scroll && r.via === "anchor") {
        const rr = resolveStep(step.target);
        if (isGood(rr, step.target)) el = rr.el;
      }
      highlight(el);
      if (step.action === "click") el.click();
      else if (step.action === "input" && step.mode === "contenteditable") {
        const landed = await setEditableValue(el, step.value);
        if (!landed) {
          results.push({ i, ok: false, reason: `Editor rejected synthetic input: ${step.label}` });
          return { ok: false, failedAt: i, results };
        }
      }
      else if (step.action === "input") setNativeValue(el, step.value);
      else if (step.action === "select") setSelectValue(el, step.value);
      results.push({ i, ok: true, via: r.via });
      await sleep(300);
    }
    return { ok: true, results };
  }

  // ---------- popup messaging ----------
  chrome.runtime.onMessage.addListener((msg, _sender, sendResponse) => {
    if (msg.cmd === "start-record") { startRecording(); sendResponse({ ok: true }); }
    else if (msg.cmd === "stop-record") { sendResponse({ steps: stopRecording() }); }
    else if (msg.cmd === "status") { sendResponse({ recording, count: steps.length }); }
    else if (msg.cmd === "replay") { replay(msg.steps).then(sendResponse); return true; }
    return true;
  });
})();
